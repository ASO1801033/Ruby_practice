multi = Array.new(9) { Array.new(9) } #二次元配列の宣言
puts "九九表を表示する！ ｽﾀ~ﾄ \\('O' \\)"

(0..8).each do |x| #行のループ
  puts "+--------------------------------------------+"
  print "|"
  (0..8).each do |y| #列のループ
    multi[x][y] = (x + 1) * (y + 1) #計算の代入
    printf("\s%2d\s|", multi[x][y].to_s) #九九表の出力
  end
  puts " " + (x + 1).to_s + "の段"
end
puts "+--------------------------------------------+"

puts "(/ 'O')/ ﾃﾞｷﾀ~"
